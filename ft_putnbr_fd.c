#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	ln;

	ln = n;
	if (ln < 0)
	{
		ln *= -1;
		write (fd, "-", 1);
	}
	if (ln > 9)
		ft_putnbr_fd ((int)(ln / 10), fd);
	write (fd, &"0123456789"[ln % 10], 1);
}
